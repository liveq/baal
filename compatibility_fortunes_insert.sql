FROM compatibility_fortunes_data;

-- 원소별 호환성 분석을 위한 뷰  
CREATE VIEW IF NOT EXISTS element_compatibility_analysis AS
SELECT 
    z1.element as element1,
    z2.element as element2,
    CASE 
        WHEN z1.element = z2.element THEN 'Same Element'
        WHEN (z1.element = 'Fire' AND z2.element = 'Air') OR (z1.element = 'Air' AND z2.element = 'Fire') THEN 'Fire-Air'
        WHEN (z1.element = 'Earth' AND z2.element = 'Water') OR (z1.element = 'Water' AND z2.element = 'Earth') THEN 'Earth-Water'
        WHEN (z1.element = 'Fire' AND z2.element = 'Water') OR (z1.element = 'Water' AND z2.element = 'Fire') THEN 'Fire-Water'
        WHEN (z1.element = 'Earth' AND z2.element = 'Air') OR (z1.element = 'Air' AND z2.element = 'Earth') THEN 'Earth-Air'
        WHEN (z1.element = 'Fire' AND z2.element = 'Earth') OR (z1.element = 'Earth' AND z2.element = 'Fire') THEN 'Fire-Earth'
        ELSE 'Other'
    END as element_combination,
    AVG(cf.overall_compatibility) as avg_compatibility,
    COUNT(*) as pair_count
FROM compatibility_fortunes_data cf
JOIN zodiac_signs z1 ON cf.zodiac1_id = z1.id
JOIN zodiac_signs z2 ON cf.zodiac2_id = z2.id
GROUP BY z1.element, z2.element
ORDER BY avg_compatibility DESC;